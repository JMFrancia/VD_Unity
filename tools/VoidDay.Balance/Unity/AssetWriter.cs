    public List<LevelEdit> LevelEdits { get; } = new();
    public List<LevelGrantsRewrite> GrantRewrites { get; } = new();
    public List<UpgradeEdit> UpgradeEdits { get; } = new();
    public List<QuestInsertion> QuestInsertions { get; } = new();

    public bool IsEmpty => Scalars.Count == 0 && RecipeInsertions.Count == 0 && LevelEdits.Count == 0
        && GrantRewrites.Count == 0 && UpgradeEdits.Count == 0
        && QuestInsertions.Count == 0 && QuestDeletions.Count == 0 && QuestEdits.Count == 0
        && QuestBlockRewrite == null;
}

/// A quest scalar knob location inside a QuestSO asset (the only per-quest fields with a surgical write path).
public enum QuestField { RewardXp, RewardMoney, RewardGems, GoalAmount, ConditionAmount }

/// One scalar edit inside a QuestSO asset: a reward/goal amount (ConditionIndex == null) or the Nth condition's amount.
public readonly record struct QuestEdit(string AssetPath, QuestField Field, int? ConditionIndex, string Old, string New);

/// A new quest to create: its config plus the freshly-allocated guid wired into GameConfig.quests.
public sealed class QuestInsertion
{
    public QuestConfig Quest { get; }
    public string Guid { get; }
    public QuestInsertion(QuestConfig quest, string guid) { Quest = quest; Guid = guid; }
}

/// A quest to remove: unwired from GameConfig.quests (block rewrite) and its .asset + .meta deleted.
public readonly record struct QuestDeletion(string Id, string Guid, string AssetPath);

public readonly record struct ScalarChange(string AssetPath, string Field, string Old, string New, bool AbsentAllowed);

/// One positional edit inside the Levels asset: a level's xpThreshold (GrantIndex == null) or a grant's amount.
public readonly record struct LevelEdit(int LevelIndex, int? GrantIndex, string Old, string New);

/// A full regeneration of one level's grant section (used when its grants change structurally). Block holds the
/// replacement lines, from the `    grants:` line down.
public readonly record struct LevelGrantsRewrite(int LevelIndex, List<string> Block);

/// One positional scalar edit inside an UpgradeSO: a tier's cost (EffectIndex == null) or an effect's value.amount.
public readonly record struct UpgradeEdit(string AssetPath, int TierIndex, int? EffectIndex, string Old, string New);

public sealed class RecipeInsertion
{
    public RecipeConfig Recipe { get; }
    public RecipeInsertion(RecipeConfig recipe) => Recipe = recipe;
}

/// A validation refusal — a clean, named abort raised before any file is touched. Program prints its
/// message and exits non-zero; it is not a bug, so it does not warrant a stack trace.
public sealed class WriteRefusedException : Exception
{
    public WriteRefusedException(string message) : base(message) { }
}
